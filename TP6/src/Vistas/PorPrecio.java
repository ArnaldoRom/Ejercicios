    private void jtfPrecioFinalKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtfPrecioFinalKeyReleased
        // TODO add your handling code here:
        try{
        borrarFila();        
        double ini=Double.parseDouble(jtfPrecioIni.getText());
        double fin=Double.parseDouble(jtfPrecioFinal.getText());
       
        for (Producto buscar : DemoModulo.productos) {
            if(buscar.getPrecio()>=ini && buscar.getPrecio()<=fin){
                modelo.addRow(new Object[]{buscar.getCodigo(),buscar.getDescripcion(),buscar.getPrecio(),buscar.getStock()});
            }
        }
        }catch(NumberFormatException v){
            JOptionPane.showMessageDialog(this,"Debe Ingresar numeros!!");
        }
    }//GEN-LAST:event_jtfPrecioFinalKeyReleased

     private void borrarFila(){
        int a=modelo.getRowCount()-1;
        for(int i=a;i>=0;i--){
            modelo.removeRow(i);
        }
     }    
    

